#include "push_swap.h"

int		get_chunk(int n)
{
	if (n <= 5)
		return (n);
	if (n <= 10)
		return (5);
	if (n <= 50)
		return (8);
	if (n <= 100)
		return (9);
	if (n <= 500)
		return (11);
	return (10);
}
